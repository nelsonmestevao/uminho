module T2_1415 where

data Heap a
  = Vazia
  | Nodo a (Heap a) (Heap a)

--------------------------------------------------------------------------------
-- Questão 1 --
--------------------------------------------------------------------------------
quantos :: Heap a -> Int
quantos Vazia = 0
quantos (Nodo r e d) = 1 + (quantos e) + (quantos d)

--------------------------------------------------------------------------------
-- Questão 2 --
--------------------------------------------------------------------------------
existe :: Ord a => a -> Heap a -> Bool
existe a Vazia = False
existe a (Nodo r e d) =
  if a == r
    then True
    else (existe a e) || (existe a d)

